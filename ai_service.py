"""
ai_service.py — Генерация персональных программ тренировок
Используем Claude API (через Anthropic SDK) или GPT.
Если нет API-ключа — используется встроенный шаблонный генератор.
"""

import os
import logging
import asyncio

logger = logging.getLogger(__name__)

# Попытка импортировать Anthropic SDK (опционально)
try:
    import anthropic
    ANTHROPIC_API_KEY = os.getenv("ANTHROPIC_API_KEY")
    HAS_AI = bool(ANTHROPIC_API_KEY)
except ImportError:
    HAS_AI = False

ANTHROPIC_API_KEY = os.getenv("ANTHROPIC_API_KEY", "")


# ══════════════════════════════════════════════════════════════════════════════════
#  ОСНОВНАЯ ФУНКЦИЯ
# ══════════════════════════════════════════════════════════════════════════════════

async def generate_workout_plan(
    age: int,
    height: int,
    weight: float,
    goal: str,
) -> str:
    """
    Генерирует персональную программу тренировок.

    Если задан ANTHROPIC_API_KEY — использует Claude AI.
    Иначе — возвращает шаблонную программу на основе данных пользователя.
    """
    if HAS_AI and ANTHROPIC_API_KEY:
        logger.info("Generating plan via Claude AI...")
        return await _generate_with_claude(age, height, weight, goal)
    else:
        logger.info("Generating plan via template engine...")
        return _generate_template_plan(age, height, weight, goal)


# ─────────────────────────────────────────────────────────────────────────────────
#  Генерация через Claude AI
# ─────────────────────────────────────────────────────────────────────────────────

async def _generate_with_claude(age: int, height: int, weight: float, goal: str) -> str:
    """Запрашивает программу тренировок у Claude"""

    # ИМТ для контекста
    bmi = round(weight / ((height / 100) ** 2), 1)

    prompt = f"""Ты — профессиональный фитнес-тренер. Составь персональную программу тренировок.

Данные клиента:
- Возраст: {age} лет
- Рост: {height} см
- Вес: {weight} кг
- ИМТ: {bmi}
- Цель: {goal}

Требования к программе:
1. Программа на 4 недели
2. Укажи 3-4 тренировки в неделю
3. Для каждой тренировки: название, упражнения с подходами/повторениями
4. Добавь рекомендации по питанию (3-4 пункта)
5. Добавь рекомендации по восстановлению
6. Форматируй с эмодзи, сделай текст мотивирующим
7. Используй Markdown для структуры

Ответ должен быть на русском языке."""

    # Запускаем синхронный SDK в пуле потоков
    def _call_api():
        client = anthropic.Anthropic(api_key=ANTHROPIC_API_KEY)
        message = client.messages.create(
            model="claude-opus-4-5",
            max_tokens=2000,
            messages=[{"role": "user", "content": prompt}]
        )
        return message.content[0].text

    loop = asyncio.get_event_loop()
    result = await loop.run_in_executor(None, _call_api)
    return result


# ─────────────────────────────────────────────────────────────────────────────────
#  Шаблонный генератор (без AI)
# ─────────────────────────────────────────────────────────────────────────────────

def _generate_template_plan(age: int, height: int, weight: float, goal: str) -> str:
    """
    Генерирует программу тренировок по шаблону на основе цели.
    Работает без API-ключей.
    """
    bmi = round(weight / ((height / 100) ** 2), 1)
    bmi_status = _get_bmi_status(bmi)

    # Выбираем план в зависимости от цели
    plans = {
        "Набор мышечной массы":    _plan_muscle(age, weight),
        "Похудение":               _plan_weight_loss(age, bmi),
        "Поддержание формы":       _plan_maintenance(age),
        "Улучшение выносливости":  _plan_endurance(age),
    }

    plan_content = plans.get(goal, _plan_maintenance(age))

    return f"""
🏋️ <b>ПЕРСОНАЛЬНАЯ ПРОГРАММА ТРЕНИРОВОК</b>
━━━━━━━━━━━━━━━━━━━━━━━━━━

👤 <b>Твои параметры:</b>
• Возраст: {age} лет
• Рост: {height} см
• Вес: {weight} кг
• ИМТ: {bmi} ({bmi_status})
• Цель: {goal}

━━━━━━━━━━━━━━━━━━━━━━━━━━
{plan_content}
━━━━━━━━━━━━━━━━━━━━━━━━━━

💡 <b>Общие рекомендации:</b>
• Разминка 5-10 мин перед каждой тренировкой
• Заминка и растяжка 5-10 мин после
• Пей 2-3 литра воды в день
• Сон не менее 7-8 часов
• Слушай своё тело — при боли остановись

Удачи в тренировках! 💪
    """.strip()


def _get_bmi_status(bmi: float) -> str:
    if bmi < 18.5: return "недостаточный вес"
    if bmi < 25:   return "норма"
    if bmi < 30:   return "избыточный вес"
    return "ожирение"


